    public static function findBySlug(string $slug): ?array
    {
        foreach (self::all() as $post) {
            if ($post['slug'] === $slug) {
                return $post;
            }
        }

        return null;
    }

    public static function featuredSeriesPosts(): array
    {
        return array_values(array_filter(self::all(), fn (array $post) => isset($post['serie'])));
    }

    public static function postsByTopic(string $topic): array
    {
        return array_values(array_filter(self::all(), fn (array $post) => ($post['tema'] ?? null) === $topic));
    }

    public static function relatedPosts(array $post, int $limit = 4): array
    {
        return self::relatedByTags($post, $limit);
    }

    public static function relatedByTags(array $post, int $limit = 6): array
    {
        $postTags = $post['tags'] ?? [];
        $postTheme = $post['tema'] ?? null;
        $postSerie = $post['serie'] ?? null;

        $scored = [];
        foreach (self::all() as $candidate) {
            if ($candidate['slug'] === $post['slug']) {
                continue;
            }

            $candidateTags = $candidate['tags'] ?? [];
            $tagMatches = count(array_intersect($postTags, $candidateTags));
            $sameSerie = $postSerie && ($candidate['serie'] ?? null) === $postSerie;
            $sameTheme = $postTheme && ($candidate['tema'] ?? null) === $postTheme;

            $score = $tagMatches * 10 + ($sameSerie ? 5 : 0) + ($sameTheme ? 2 : 0);

            if ($score === 0) {
                continue;
            }

            $scored[] = ['post' => $candidate, 'score' => $score];
        }

        usort($scored, fn ($a, $b) => $b['score'] <=> $a['score']);

        return array_map(fn ($item) => $item['post'], array_slice($scored, 0, $limit));
    }

    public static function topics(): array
    {
        $metadata = self::topicsMetadata();
        $out = [];
        foreach ($metadata as $slug => $meta) {
            $out[] = array_merge($meta, [
                'slug' => $slug,
                'count' => count(self::postsByTopic($slug)),
            ]);
        }

        return $out;
    }

    public static function findTopic(string $slug): ?array
    {
        $metadata = self::topicsMetadata();
        if (!isset($metadata[$slug])) {
            return null;
        }

        return array_merge($metadata[$slug], [
            'slug' => $slug,
            'count' => count(self::postsByTopic($slug)),
        ]);
    }

