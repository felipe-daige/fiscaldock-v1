<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">1. Conferência cadastral</h2>

<p>Antes de olhar tributo, confirme se a base está confiável:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li>CNPJ e IE dos participantes</li>
    <li>duplicidade de cadastro</li>
    <li>dados mínimos de identificação do estabelecimento</li>
    <li>consistência entre período informado e documentos escriturados</li>
</ul>

<p>Uma base cadastral ruim contamina toda a revisão posterior.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">2. Integridade dos documentos</h2>

<p>O segundo passo é verificar se os documentos esperados realmente entraram no arquivo e se os valores principais fazem sentido. Aqui vale conferir:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li>notas de entrada e saída relevantes para o período</li>
    <li>documentos de transporte ou serviços, quando aplicável</li>
    <li>valores totais de documento, base e imposto</li>
    <li>eventuais notas canceladas, denegadas ou escrituradas indevidamente</li>
</ul>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">3. Classificação fiscal</h2>

<p>Depois da integridade básica, o contador precisa olhar a classificação. Os pontos com maior retorno de revisão são:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li>CFOP coerente com a natureza da operação</li>
    <li>CST ou CSOSN aderente ao regime e ao tipo de documento</li>
    <li>NCM compatível com o item e com o histórico do cliente</li>
    <li>regras específicas de ST, monofásico, isenção ou alíquota zero, quando existirem</li>
</ul>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">4. Coerência da apuração</h2>

<p>Não basta a nota estar escriturada. O fechamento precisa refletir a realidade tributária do período. Por isso, vale checar se:</p>

<ol class="list-decimal pl-6 space-y-3 my-4">
    <li>os totais por bloco batem com o movimento esperado do cliente</li>
    <li>créditos relevantes possuem lastro documental</li>
    <li>ajustes e estornos possuem justificativa</li>
    <li>variações fora do padrão mensal foram investigadas</li>
</ol>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">5. Validação final antes da transmissão</h2>

<p>Na etapa final, o escritório deve registrar quem revisou, quais alertas foram tratados e o que ficou como ponto de acompanhamento para o mês seguinte. Isso cria histórico e evita que o mesmo erro reapareça sem controle.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Conclusão</h2>

<p>Checklist não é burocracia. É o instrumento mais simples para transformar a revisão fiscal em um processo confiável. Quando essa rotina está documentada, o escritório reduz a dependência de heróis, melhora a previsibilidade da entrega e ganha escala sem perder critério técnico.</p>

