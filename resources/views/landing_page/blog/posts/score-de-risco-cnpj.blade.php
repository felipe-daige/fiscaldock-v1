<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">O que o score não resolve</h2>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li>não substitui diligência contratual</li>
    <li>não captura relação entre partes (contratos, grupos econômicos)</li>
    <li>não avalia aderência da operação ao que o CNPJ declara fazer</li>
    <li>não dispensa a leitura das certidões na hora do contrato</li>
</ul>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Conclusão</h2>

<p>Score é uma ferramenta de triagem, não de julgamento final. Quando o escritório usa como filtro para onde olhar primeiro, ganha escala. Quando usa como decisão automática, erra nos casos que mais importam.</p>
