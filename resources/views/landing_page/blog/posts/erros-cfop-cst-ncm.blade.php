<p>CFOP, CST e NCM aparecem em praticamente toda revisão fiscal, mas ainda são tratados por muitos times como campos de preenchimento. Não são. Eles definem a leitura tributária da operação e, quando errados, afetam crédito, apuração e coerência da obrigação inteira.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Erro de CFOP: o problema começa na natureza da operação</h2>

<p>CFOP errado costuma surgir quando a equipe replica um código antigo sem revisar o contexto atual da nota. O efeito é imediato: a operação passa a contar uma história fiscal diferente da realidade.</p>

<p>Isso gera risco especialmente em casos de:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li>devolução tratada como compra ou venda comum</li>
    <li>operações interestaduais classificadas como internas</li>
    <li>retorno, remessa ou bonificação com código inadequado</li>
    <li>prestação de serviço ou transporte mal enquadrada</li>
</ul>

