<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Erro de CST: o reflexo direto no imposto</h2>

<p>Se o CFOP descreve a operação, o CST ajuda a definir o tratamento tributário. Quando o CST está incorreto, o arquivo pode demonstrar crédito indevido, tributação a menor ou base mal formada.</p>

<p>Na rotina do escritório, isso aparece com frequência em:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li>itens reaproveitados de cadastro sem revisar o regime</li>
    <li>operações monofásicas, substituição tributária ou isenção tratadas de forma genérica</li>
    <li>documentos de entrada com crédito apropriado sem lastro suficiente</li>
    <li>divergência entre o CST do documento e a lógica da apuração</li>
</ul>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Erro de NCM: o risco silencioso</h2>

<p>O NCM raramente chama atenção no fechamento, mas ele influencia a classificação do item, regras específicas e a coerência do cadastro de produtos. Quando fica desatualizado ou é usado por aproximação, o escritório passa a revisar um dado que já nasceu frágil.</p>

<p>Os sinais mais comuns são:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li>mesmo item com NCM diferente em meses próximos</li>
    <li>produto sensível com descrição genérica demais</li>
    <li>NCM incompatível com o tipo de mercadoria declarada</li>
    <li>cadastro antigo herdado sem validação recente</li>
</ul>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Como reduzir esse risco na prática</h2>

<p>O melhor caminho é parar de revisar esses campos apenas no momento da entrega. O escritório ganha muito mais quando cria uma rotina de validação recorrente:</p>

<ol class="list-decimal pl-6 space-y-3 my-4">
    <li>mapear os códigos mais usados por cliente</li>
    <li>comparar mudanças bruscas em relação ao histórico</li>
    <li>cruzar a classificação com o XML e com o cadastro do item</li>
    <li>tratar exceções relevantes antes do fechamento final</li>
</ol>

