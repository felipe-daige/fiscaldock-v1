<p>Para contadores e escritórios contábeis, monitorar riscos fiscais não é opcional. É o que separa um escritório reativo de um proativo. Fornecedores irregulares, notas com problemas e créditos tributários indevidos podem gerar multas pesadas para seus clientes.</p>

<p>Neste artigo, listamos os <strong>5 riscos fiscais mais comuns</strong> que todo contador deveria monitorar continuamente.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">1. Fornecedor com CNPJ irregular</h2>

<p>Um fornecedor com CNPJ baixado, suspenso ou inapto na Receita Federal pode invalidar créditos tributários inteiros. Se o seu cliente tomou crédito de ICMS ou PIS/COFINS de uma nota emitida por um fornecedor nessa situação, o fisco pode glosar o crédito e aplicar multa.</p>

<p><strong>Como monitorar:</strong> consultas periódicas à Receita Federal via CNPJ. No FiscalDock, isso é feito automaticamente com consultas em lote e alertas quando o status muda.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">2. Inscrição Estadual (IE) suspensa ou cancelada</h2>

<p>A IE é obrigatória para operações com ICMS. Se um fornecedor tem IE cancelada ou suspensa no SINTEGRA, as notas emitidas por ele podem ser consideradas inidôneas, comprometendo créditos de ICMS.</p>

<p><strong>Como monitorar:</strong> consulta ao SINTEGRA por estado. O FiscalDock integra com o SINTEGRA para verificar a situação da IE de cada participante extraído do SPED.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">3. Empresa listada no CEIS</h2>

<p>O CEIS é um cadastro federal de empresas punidas por fraude em licitações, contratos públicos ou outras irregularidades. Operar com fornecedores listados no CEIS pode gerar risco reputacional e legal, especialmente para empresas que participam de licitações.</p>

<p><strong>Como monitorar:</strong> consulta ao Portal da Transparência. O FiscalDock verifica automaticamente se participantes do SPED estão listados no CEIS.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">4. Fornecedor optante pelo Simples Nacional</h2>

<p>Quando um fornecedor é optante pelo Simples Nacional, o regime de apuração de PIS/COFINS muda. Créditos tomados indevidamente de fornecedores do Simples podem ser glosados em auditoria.</p>

<p><strong>Como monitorar:</strong> consulta à Receita Federal sobre opção pelo Simples. O FiscalDock enriquece os dados dos participantes com essa informação automaticamente.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">5. Divergências entre SPED e notas fiscais</h2>

<p>Divergências entre os valores declarados no SPED (EFD ICMS/IPI ou PIS/COFINS) e os dados reais das notas fiscais são um dos principais gatilhos para malha fiscal. Valores de <code>VL_DOC</code> inconsistentes, CFOPs incorretos ou notas não declaradas geram alertas na SEFAZ.</p>

<p><strong>Como monitorar:</strong> importação e análise do SPED com cruzamento de dados. O FiscalDock extrai automaticamente notas por bloco (A, C, D) e permite análise por CFOP, participante e período nos dashboards.</p>

