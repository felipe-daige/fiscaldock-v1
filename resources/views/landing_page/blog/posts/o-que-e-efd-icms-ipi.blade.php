<p>A EFD ICMS/IPI é uma das obrigações que mais consomem tempo do escritório contábil porque concentra documentos, classificações e reflexos diretos na apuração. Para revisar bem, o contador precisa entender não apenas o conceito, mas também o tipo de dado que essa escrituração leva para o fechamento mensal.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">O que entra na EFD ICMS/IPI</h2>

<p>De forma prática, ela reúne a escrituração de operações com mercadorias, documentos fiscais e elementos da apuração de ICMS e IPI. É onde o escritório costuma concentrar a maior parte da conferência:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
