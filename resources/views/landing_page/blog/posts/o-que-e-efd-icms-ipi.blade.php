<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Por que isso pesa tanto na rotina do contador</h2>

<p>Isso acontece porque a EFD ICMS/IPI não é apenas um arquivo para transmissão. Ela funciona como a consolidação de tudo o que foi escriturado no mês. Se houver erro de documento, classificação ou base de cálculo, o problema tende a se propagar para a apuração e para o risco fiscal do cliente.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Onde o escritório deve olhar primeiro</h2>

<ol class="list-decimal pl-6 space-y-3 my-4">
    <li>coerência entre o movimento do cliente e o volume de documentos</li>
    <li>cadastro dos principais participantes</li>
    <li>CFOP e CST das operações mais recorrentes</li>
    <li>valores de documento e base de imposto</li>
    <li>variações relevantes em relação aos meses anteriores</li>
</ol>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Conclusão</h2>

<p>Entender o que é a EFD ICMS/IPI ajuda o escritório a sair da revisão genérica e entrar em uma análise mais estruturada. O ganho real não está apenas em transmitir sem erro, mas em criar uma rotina de revisão capaz de detectar exceções antes que elas virem retrabalho ou questionamento fiscal.</p>
