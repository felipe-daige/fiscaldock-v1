<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Como começar sem virar uma implantação de meses</h2>

<ol class="list-decimal pl-6 space-y-3 my-4">
    <li>centralize os XMLs em um repositório único por cliente</li>
    <li>rode verificação em lote contra a SEFAZ antes do fechamento</li>
    <li>cruze o resultado com o que já foi escriturado na EFD</li>
    <li>transforme as divergências em fila de trabalho — não em e-mail avulso</li>
</ol>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Conclusão</h2>

<p>Clearance deixou de ser exótico. Com o fisco cruzando documento por documento em segundos, revisar NF-e em lote virou infra básica. O escritório que faz esse processo antes da entrega reduz glosa, retificação e o tempo que a equipe gasta respondendo a questionamento depois que o problema já aconteceu.</p>
