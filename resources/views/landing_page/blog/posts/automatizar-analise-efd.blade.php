<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">O que não deve ser terceirizado para a automação</h2>

<p>Uma boa automação não substitui a análise fiscal. Ela prepara a base para que a revisão seja mais rápida e melhor direcionada. O contador continua sendo essencial para:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li>interpretar exceções fora do padrão</li>
    <li>decidir sobre tratamento tributário em cenários específicos</li>
    <li>validar ajustes, estornos e créditos sensíveis</li>
    <li>orientar o cliente sobre causas recorrentes de erro</li>
</ul>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Como fica uma rotina mais eficiente</h2>

<p>Em vez de abrir o arquivo-texto, navegar por registros e consolidar informações na mão, o fluxo ideal passa a ser:</p>

<ol class="list-decimal pl-6 space-y-3 my-4">
    <li>importar a EFD em uma base organizada</li>
    <li>visualizar documentos, participantes e totais sem depender do TXT bruto</li>
    <li>receber alertas de inconsistências e variações relevantes</li>
    <li>atuar apenas nas exceções que exigem decisão técnica</li>
</ol>

