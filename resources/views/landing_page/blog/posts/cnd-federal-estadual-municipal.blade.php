<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">O que costuma dar errado</h2>

<ol class="list-decimal pl-6 space-y-3 my-4">
    <li>achar que a Federal cobre tudo — não cobre ISS nem ICMS</li>
    <li>guardar certidões vencidas como se ainda valessem</li>
    <li>não monitorar renovação — CND tem validade curta</li>
    <li>tratar "positiva com efeito de negativa" como positiva</li>
</ol>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Conclusão</h2>

<p>As três CNDs resolvem problemas diferentes e todas prescrevem rápido. O escritório que organiza monitoramento contínuo das três garante que cliente e fornecedor não percam contrato por certidão vencida no momento errado.</p>
