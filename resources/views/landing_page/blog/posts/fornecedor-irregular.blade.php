<p>Um fornecedor com CNPJ baixado, IE suspensa ou listado no CEIS pode custar caro ao seu cliente. Créditos tributários tomados de fornecedores irregulares podem ser glosados em auditoria, resultando em multas pesadas e juros.</p>

<p>A boa notícia é que é possível identificar esses riscos antes que o fisco o faça. Veja como.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Por que fornecedores irregulares são um risco</h2>

<p>Quando seu cliente compra de um fornecedor e toma crédito de ICMS, PIS ou COFINS, ele está assumindo que aquele fornecedor está regular perante o fisco. Se não estiver, o crédito pode ser considerado indevido.</p>

<p>Os cenários mais comuns são:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li><strong>CNPJ baixado ou inapto:</strong> a empresa deixou de existir ou foi declarada inapta pela Receita Federal. Notas emitidas após o encerramento podem ser consideradas inidôneas.</li>
    <li><strong>IE cancelada ou suspensa:</strong> a inscrição estadual é obrigatória para operações com ICMS. Sem IE válida, o crédito de ICMS pode ser glosado.</li>
    <li><strong>Empresa no CEIS:</strong> empresas listadas no Cadastro de Empresas Inidôneas e Suspensas foram punidas por fraudes. Operar com elas gera risco legal e reputacional.</li>
    <li><strong>Optante pelo Simples indevidamente:</strong> se o fornecedor é do Simples Nacional, o regime de crédito de PIS/COFINS muda. Tomar crédito integral nesses casos é uma irregularidade comum.</li>
</ul>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Como identificar na prática</h2>

<h3 class="text-xl font-bold text-gray-900 mt-8 mb-3">1. Consulta à Receita Federal (CNPJ)</h3>

<p>A consulta de CNPJ na Receita Federal revela a situação cadastral, o QSA, os CNAEs e o endereço. É a primeira verificação que deve ser feita.</p>

<h3 class="text-xl font-bold text-gray-900 mt-8 mb-3">2. Consulta ao SINTEGRA</h3>

<p>O SINTEGRA permite verificar a IE do fornecedor por estado. Uma IE cancelada ou nula indica que o fornecedor não pode emitir notas com ICMS.</p>

<h3 class="text-xl font-bold text-gray-900 mt-8 mb-3">3. Consulta ao CEIS</h3>

<p>O Portal da Transparência disponibiliza o CEIS com todas as empresas punidas. É uma consulta essencial para due diligence, especialmente se o cliente participa de licitações.</p>

<h3 class="text-xl font-bold text-gray-900 mt-8 mb-3">4. Verificação de Simples Nacional</h3>

<p>A consulta sobre opção pelo Simples Nacional confirma se o fornecedor é optante e desde quando. Isso impacta diretamente o cálculo de créditos de PIS/COFINS.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">O problema de fazer isso manualmente</h2>

<p>Para um único fornecedor, essas consultas levam alguns minutos. Mas um SPED típico pode ter dezenas ou centenas de participantes. Verificar cada um manualmente, em diferentes fontes, é inviável na prática.</p>

<p>É por isso que muitos escritórios só descobrem o problema quando o fisco bate à porta.</p>

