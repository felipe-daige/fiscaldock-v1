# FiscalDock

> Radar de riscos fiscais para contadores, escritórios contábeis e empresas. Monitora CNPJs, consolida consultas de compliance e ajuda a detectar inconsistências no SPED antes da malha fiscal, com créditos pré-pagos e sem mensalidade.

## O que o FiscalDock faz

- **Monitoramento de CNPJs**: situação cadastral, regime tributário, Simples Nacional e sinais de risco de participantes
- **Consultas de compliance**: CNPJ completo, CND, CNDT, FGTS e outras fontes fiscais em fluxo consolidado
- **Alertas e inconsistências no SPED**: cruzamentos entre EFD, XML, apurações, participantes e classificações fiscais
- **Importação EFD ICMS/IPI**: leitura dos blocos C, D, E, H — extração de notas, itens, apuração de ICMS e inventário
- **Importação EFD Contribuições**: blocos A, M, F — apuração de PIS/COFINS, retenções na fonte e serviços tomados
- **Clearance de NF-e**: consulta e validação de documentos fiscais contra fontes oficiais, com produto ainda em evolução
- **BI Fiscal**: dashboards de cruzamento (apuração × notas), ranking de participantes, análise por CFOP
- **Modelo comercial**: créditos pré-pagos, sem assinatura mensal

## Para quem

Contadores, escritórios contábeis, departamentos fiscais, compradores e empresas brasileiras que precisam monitorar riscos fiscais, reduzir retrabalho em revisão mensal de EFD e automatizar consultas em lote de participantes.

